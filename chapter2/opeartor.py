a = 12
b = 4
# ARITHMETIC OPERATORS
print('the value of a+b is ', a+b)
print('the value of a-b is ', a-b)
print('the value of a*b is ', a*b)
print('the value of a/b is ', a/b)

# ASSIGNMENT OPERATOR
a=34
a+=12
a-=12
a*=12
a/=12
print(a)

# COMPARISON OPERATOR
# b = (14>7)
# b = (14>=7)
# b = (14<7)
# b = (14<=7)
b = (14==7)
# b = (14!=7)
print(b)

# logical operator
bool1=True
bool2=False
print("the value of bool1 and bool2 is ", (bool1 and bool2))
print("the value of bool1 or bool2 is ", (bool1 or bool2))
# NOT LOGICAL OPERATOR IS USED FOR ONLY 1 VARIABLE
print("the value of not bool2 is ", (not bool2))
